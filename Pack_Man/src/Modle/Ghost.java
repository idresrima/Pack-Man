package Modle;

import java.util.Objects;


public class Ghost {

	public int id;
	public int regularSpeed;
	public int levelUpspeed;
	public String ghost;
	public String ghost_back;
	public String ghost_left;
	public String ghost_right;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRegularSpeed() {
		return regularSpeed;
	}
	public void setRegularSpeed(int regularSpeed) {
		this.regularSpeed = regularSpeed;
	}
	public int getLevelUpspeed() {
		return levelUpspeed;
	}
	public void setLevelUpspeed(int levelUpspeed) {
		this.levelUpspeed = levelUpspeed;
	}
	public String getGhost() {
		return ghost;
	}
	public void setGhost(String ghost) {
		this.ghost = ghost;
	}
	public String getGhost_back() {
		return ghost_back;
	}
	public void setGhost_back(String ghost_back) {
		this.ghost_back = ghost_back;
	}
	public String getGhost_left() {
		return ghost_left;
	}
	public void setGhost_left(String ghost_left) {
		this.ghost_left = ghost_left;
	}
	public String getGhost_right() {
		return ghost_right;
	}
	public void setGhost_right(String ghost_right) {
		this.ghost_right = ghost_right;
	}
	
	


}
